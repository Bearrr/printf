#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int n;
	int a;

	i = 0;
	n = 1;
	a = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-')
		n = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		a = (a * 10) + (str[i] - '0') * n;
		i++;
	}
	return (a);
}
